//we export pure functions

export function addTwoNumbers(sumNum1, sumNum2) {
    //scope...
    return sumNum1 + sumNum2;
}

export function difTwoNumbers(difNum1, difNum2) {
    //scope...
    return difNum1 - difNum2;
}

export function multTwoNumbers(multNum1, multNum2) {
    //scope...
    return multNum1 * multNum2;
}

export function divTwoNumbers(divNum1, divNum2) {
    //scope...
    return divNum1 / divNum2;
}